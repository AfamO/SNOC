    public void UpdateInforCaller()
 {
     String name =nameTF.getText();
     String user =useridTF.getText();
     String pass = passTF.getText();
     String vpass=vpassTF.getText();
     
     String status=staTF.getText();
     if(name.equals("")||name==null)
     {
         JOptionPane.showMessageDialog(null,"Name field Should not be Empty");
     }
     if(user.equals("")||user==null)
     {
         JOptionPane.showMessageDialog(null,"User id Should not be Empty");
     }else if(pass.equals("")||pass==null)
     {
         JOptionPane.showMessageDialog(null, "Password Should not be Empty");
     }else if(status.equals("")||status==null)
     {
         JOptionPane.showMessageDialog(null, "Status should not be Empty");
     }else
     {
         if(!pass.equals(vpass) )
         {
             //   || pass!=vpass
           JOptionPane.showMessageDialog(null, "Password MisMatch\n please Check you Password");  
         }else{ 
       
         Reconsoft data=new Reconsoft();
         data.updateAdmin(name,user, pass, status);
         JOptionPane.showMessageDialog(null, "User Information Updated Successfully");
        
         this.ResetB();
         }
     }
 }
    
    
    
